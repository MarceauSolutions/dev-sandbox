#!/usr/bin/env python3
"""
Accuracy Calculator - Compare Algorithm Estimates vs Actual Quotes

Reads:
- estimated-quotes.csv (generated by run_algorithm.py)
- actual-quotes.csv (filled in by human after collecting real quotes)

Outputs:
- ACCURACY-REPORT.md (comprehensive analysis)
- accuracy-metrics.csv (raw metrics data)

This script is run by human AFTER collecting actual quotes.
"""

import csv
import os
import sys
from datetime import datetime
from typing import Dict, List, Tuple
import statistics


def load_estimates(path: str) -> Dict:
    """Load algorithm estimates by route_id"""
    estimates = {}
    with open(path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            route_id = row['route_id']
            estimates[route_id] = row
    return estimates


def load_actuals(path: str) -> Dict:
    """Load actual quotes by route_id"""
    actuals = {}
    with open(path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            route_id = row['route_id']
            actuals[route_id] = row
    return actuals


def calculate_error(estimated: float, actual: float) -> Dict:
    """
    Calculate various error metrics

    Returns:
        dict with absolute_error, percentage_error, within_20_pct
    """
    if actual == 0:
        return {
            'absolute_error': None,
            'percentage_error': None,
            'within_20_pct': None
        }

    absolute_error = abs(estimated - actual)
    percentage_error = (absolute_error / actual) * 100
    within_20_pct = percentage_error <= 20

    return {
        'absolute_error': round(absolute_error, 2),
        'percentage_error': round(percentage_error, 1),
        'within_20_pct': within_20_pct
    }


def analyze_accuracy(estimates: Dict, actuals: Dict) -> Dict:
    """
    Perform comprehensive accuracy analysis

    Returns:
        dict with all metrics and analysis
    """
    results = {
        'uber_errors': [],
        'lyft_errors': [],
        'recommendation_accuracy': [],
        'details': []
    }

    for route_id, estimate in estimates.items():
        actual = actuals.get(route_id)

        if not actual:
            continue

        # Skip if actual quotes not filled in
        if not actual.get('actual_uber') or not actual.get('actual_lyft'):
            continue

        try:
            # Parse values
            est_uber = float(estimate['uber_estimate'])
            est_lyft = float(estimate['lyft_estimate'])
            act_uber = float(actual['actual_uber'])
            act_lyft = float(actual['actual_lyft'])
            est_recommendation = estimate['recommendation']

            # Calculate errors
            uber_error = calculate_error(est_uber, act_uber)
            lyft_error = calculate_error(est_lyft, act_lyft)

            results['uber_errors'].append(uber_error)
            results['lyft_errors'].append(lyft_error)

            # Check if our recommendation was correct
            actual_best = 'lyft' if act_lyft < act_uber else 'uber'
            recommendation_correct = est_recommendation == actual_best
            results['recommendation_accuracy'].append(recommendation_correct)

            # Detailed results per route
            results['details'].append({
                'route_id': route_id,
                'city': estimate['city'],
                'distance_category': estimate['distance_category'],
                'est_uber': est_uber,
                'act_uber': act_uber,
                'uber_error_pct': uber_error['percentage_error'],
                'est_lyft': est_lyft,
                'act_lyft': act_lyft,
                'lyft_error_pct': lyft_error['percentage_error'],
                'est_recommendation': est_recommendation,
                'actual_best': actual_best,
                'recommendation_correct': recommendation_correct,
                'surge_active': actual.get('surge_active', 'unknown'),
            })

        except (ValueError, KeyError) as e:
            print(f"Skipping route {route_id}: {e}")
            continue

    return results


def calculate_summary_metrics(results: Dict) -> Dict:
    """Calculate summary statistics from analysis results"""
    uber_pct_errors = [e['percentage_error'] for e in results['uber_errors']
                       if e['percentage_error'] is not None]
    lyft_pct_errors = [e['percentage_error'] for e in results['lyft_errors']
                       if e['percentage_error'] is not None]

    uber_within_20 = sum(1 for e in results['uber_errors']
                         if e.get('within_20_pct'))
    lyft_within_20 = sum(1 for e in results['lyft_errors']
                         if e.get('within_20_pct'))

    total_routes = len(results['details'])
    correct_recommendations = sum(results['recommendation_accuracy'])

    metrics = {
        'total_routes_analyzed': total_routes,
        'uber': {
            'mean_error_pct': round(statistics.mean(uber_pct_errors), 1) if uber_pct_errors else None,
            'median_error_pct': round(statistics.median(uber_pct_errors), 1) if uber_pct_errors else None,
            'max_error_pct': round(max(uber_pct_errors), 1) if uber_pct_errors else None,
            'min_error_pct': round(min(uber_pct_errors), 1) if uber_pct_errors else None,
            'within_20_pct_count': uber_within_20,
            'within_20_pct_rate': round(100 * uber_within_20 / total_routes, 1) if total_routes else None,
        },
        'lyft': {
            'mean_error_pct': round(statistics.mean(lyft_pct_errors), 1) if lyft_pct_errors else None,
            'median_error_pct': round(statistics.median(lyft_pct_errors), 1) if lyft_pct_errors else None,
            'max_error_pct': round(max(lyft_pct_errors), 1) if lyft_pct_errors else None,
            'min_error_pct': round(min(lyft_pct_errors), 1) if lyft_pct_errors else None,
            'within_20_pct_count': lyft_within_20,
            'within_20_pct_rate': round(100 * lyft_within_20 / total_routes, 1) if total_routes else None,
        },
        'recommendation': {
            'correct_count': correct_recommendations,
            'accuracy_rate': round(100 * correct_recommendations / total_routes, 1) if total_routes else None,
        },
        'overall': {
            'combined_within_20_pct': round(100 * (uber_within_20 + lyft_within_20) / (2 * total_routes), 1) if total_routes else None,
        }
    }

    return metrics


def analyze_by_category(results: Dict) -> Dict:
    """Analyze accuracy by distance category and city"""
    by_distance = {}
    by_city = {}

    for detail in results['details']:
        # By distance category
        cat = detail['distance_category']
        if cat not in by_distance:
            by_distance[cat] = {'uber_errors': [], 'lyft_errors': [], 'correct': 0, 'total': 0}

        by_distance[cat]['uber_errors'].append(detail['uber_error_pct'])
        by_distance[cat]['lyft_errors'].append(detail['lyft_error_pct'])
        by_distance[cat]['correct'] += 1 if detail['recommendation_correct'] else 0
        by_distance[cat]['total'] += 1

        # By city
        city = detail['city']
        if city not in by_city:
            by_city[city] = {'uber_errors': [], 'lyft_errors': [], 'correct': 0, 'total': 0}

        by_city[city]['uber_errors'].append(detail['uber_error_pct'])
        by_city[city]['lyft_errors'].append(detail['lyft_error_pct'])
        by_city[city]['correct'] += 1 if detail['recommendation_correct'] else 0
        by_city[city]['total'] += 1

    # Calculate summaries
    distance_summary = {}
    for cat, data in by_distance.items():
        distance_summary[cat] = {
            'uber_mean_error': round(statistics.mean(data['uber_errors']), 1) if data['uber_errors'] else None,
            'lyft_mean_error': round(statistics.mean(data['lyft_errors']), 1) if data['lyft_errors'] else None,
            'recommendation_accuracy': round(100 * data['correct'] / data['total'], 1) if data['total'] else None,
            'total_routes': data['total'],
        }

    city_summary = {}
    for city, data in by_city.items():
        city_summary[city] = {
            'uber_mean_error': round(statistics.mean(data['uber_errors']), 1) if data['uber_errors'] else None,
            'lyft_mean_error': round(statistics.mean(data['lyft_errors']), 1) if data['lyft_errors'] else None,
            'recommendation_accuracy': round(100 * data['correct'] / data['total'], 1) if data['total'] else None,
            'total_routes': data['total'],
        }

    return {
        'by_distance': distance_summary,
        'by_city': city_summary,
    }


