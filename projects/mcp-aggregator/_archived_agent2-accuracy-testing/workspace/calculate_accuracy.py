def save_metrics_csv(results: Dict, output_path: str):
    """Save detailed metrics to CSV for further analysis"""
    fieldnames = [
        'route_id', 'city', 'distance_category',
        'est_uber', 'act_uber', 'uber_error_pct',
        'est_lyft', 'act_lyft', 'lyft_error_pct',
        'est_recommendation', 'actual_best', 'recommendation_correct',
        'surge_active'
    ]

    with open(output_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(results['details'])

    print(f"Metrics CSV saved: {output_path}")


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    output_dir = os.path.join(os.path.dirname(script_dir), 'output')

    estimates_path = os.path.join(script_dir, 'estimated-quotes.csv')
    actuals_path = os.path.join(script_dir, 'actual-quotes.csv')  # Human fills from template
    report_path = os.path.join(output_dir, 'ACCURACY-REPORT.md')
    metrics_csv_path = os.path.join(script_dir, 'accuracy-metrics.csv')

    # Check for required files
    if not os.path.exists(estimates_path):
        print(f"ERROR: {estimates_path} not found!")
        print("Run run_algorithm.py first.")
        sys.exit(1)

    if not os.path.exists(actuals_path):
        print(f"ERROR: {actuals_path} not found!")
        print("")
        print("INSTRUCTIONS:")
        print("1. Copy actual-quotes-template.csv to actual-quotes.csv")
        print("2. Collect real Uber/Lyft quotes for each route")
        print("3. Fill in actual_uber and actual_lyft columns")
        print("4. Re-run this script")
        print("")
        print("See output/QUOTE-COLLECTION-GUIDE.md for detailed instructions.")
        sys.exit(1)

    # Load data
    print("Loading data...")
    estimates = load_estimates(estimates_path)
    actuals = load_actuals(actuals_path)

    # Analyze
    print("Analyzing accuracy...")
    results = analyze_accuracy(estimates, actuals)

    if not results['details']:
        print("ERROR: No valid comparison data found!")
        print("Make sure actual-quotes.csv has actual_uber and actual_lyft filled in.")
        sys.exit(1)

    # Calculate metrics
    metrics = calculate_summary_metrics(results)
    categories = analyze_by_category(results)

